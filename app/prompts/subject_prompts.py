DEFAULT_SUBJECT_INSTRUCTION = (
    "Решай задачу в общем школьном формате. "
    "Уделяй внимание понятности, проверке ответа и указанию используемых формул."
)


def get_subject_instruction(subject: str) -> str:
    """Вернуть инструкции для выбранного предмета."""
    subject_key = subject.strip()
    return SUBJECT_SPECIFIC_INSTRUCTIONS.get(subject_key, DEFAULT_SUBJECT_INSTRUCTION)


def normalize_detected_subject(subject: str) -> str:
    """Преобразовать название предмета, полученное от модели, в каноничный вариант."""
    if not isinstance(subject, str):
        return ""

    normalized = subject.strip().lower()
    if not normalized:
        return ""

    for alias, canonical in SUBJECT_ALIASES.items():
        if alias in normalized:
            return canonical

    return subject.strip()

    return subject.strip()
